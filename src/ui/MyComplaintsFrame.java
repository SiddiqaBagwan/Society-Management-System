    public void loadComplaints(String email) {
        try {
            ResultSet rs = dao.UserDAO.getComplaints(email);

            javax.swing.table.DefaultTableModel model
                    = (javax.swing.table.DefaultTableModel) complaintTable.getModel();

            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
    rs.getInt("complaint_id"),   // 🔥 ADD THIS
    rs.getString("description"),
    rs.getString("priority"),
    rs.getString("status"),
    rs.getDate("date_created")
});
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

