    private void deleteComplaintBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteComplaintBtnActionPerformed
        // TODO add your handling code here:
    

    int row = complaintTable.getSelectedRow();

    if(row == -1) {
        JOptionPane.showMessageDialog(this, "Select complaint first");
        return;
    }

int complaintId = Integer.parseInt(complaintTable.getValueAt(row, 0).toString());
    int confirm = JOptionPane.showConfirmDialog(this,
            "Delete this complaint?",
            "Confirm",
            JOptionPane.YES_NO_OPTION);

    if(confirm == JOptionPane.YES_OPTION) {

        dao.UserDAO.deleteComplaint(complaintId, currentEmail);

        JOptionPane.showMessageDialog(this, "Complaint Deleted");

        loadComplaints(currentEmail); // 🔥 FIX
    }


    }//GEN-LAST:event_deleteComplaintBtnActionPerformed

