    public void loadUsers(String type) {
        try {

            ResultSet rs;

            if (type.equals("pending")) {
                rs = dao.UserDAO.getPendingUsers();
            } else {
                rs = dao.UserDAO.getAllUsers();
            }

            javax.swing.table.DefaultTableModel model
                    = (javax.swing.table.DefaultTableModel) usersTable.getModel();

            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("user_id"),
                    rs.getString("name"),
                    rs.getString("email"),
                    rs.getString("phone"),
                    rs.getString("role"),
                    rs.getString("approval_status") + " / "
                    + (rs.getInt("is_active") == 1 ? "Active" : "Inactive"),
                    rs.getTimestamp("created_at")
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

