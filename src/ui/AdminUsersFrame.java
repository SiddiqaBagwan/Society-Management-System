    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        // TODO add your handling code here:
        int row = usersTable.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a user first");
            return;
        }

        int userId = Integer.parseInt(usersTable.getValueAt(row, 0).toString());

// 🔥 FIXED COLUMN INDEX
        String status = usersTable.getValueAt(row, 5).toString();

        if (!status.toLowerCase().contains("pending")) {
            JOptionPane.showMessageDialog(this, "User is not pending");
            return;
        }

        dao.UserDAO.approveUser(userId);

        JOptionPane.showMessageDialog(this, "User Approved");

        loadUsers("pending");
    }//GEN-LAST:event_approveBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int row = usersTable.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select user first");
            return;
        }

        int userId = Integer.parseInt(usersTable.getValueAt(row, 0).toString());

        String currentName = usersTable.getValueAt(row, 1).toString();
        String currentPhone = usersTable.getValueAt(row, 3).toString();

        String newName = JOptionPane.showInputDialog(this, "Enter new name", currentName);
        String newPhone = JOptionPane.showInputDialog(this, "Enter new phone", currentPhone);

        if (newName == null || newPhone == null) {
            return;
        }

        // validation
        if (!newName.matches("[A-Za-z ]+")) {
            JOptionPane.showMessageDialog(this, "Invalid name");
            return;
        }

        if (!newPhone.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this, "Invalid phone");
            return;
        }

        dao.UserDAO.updateUser(userId, newName, newPhone);

        JOptionPane.showMessageDialog(this, "User Updated");

        loadUsers("all");
    }//GEN-LAST:event_updateBtnActionPerformed

    private void changePasswordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordBtnActionPerformed
        // TODO add your handling code here:
        int row = usersTable.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select user first");
            return;
        }

        int userId = Integer.parseInt(usersTable.getValueAt(row, 0).toString());

        String newPassword = JOptionPane.showInputDialog(this, "Enter new password");

        if (newPassword == null) {
            return;
        }

        if (newPassword.length() < 6) {
            JOptionPane.showMessageDialog(this, "Password must be at least 6 characters");
            return;
        }

        dao.UserDAO.updatePassword(userId, newPassword);

        JOptionPane.showMessageDialog(this, "Password Updated");
    }//GEN-LAST:event_changePasswordBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:

        int row = usersTable.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select user first");
            return;
        }

        int userId = Integer.parseInt(usersTable.getValueAt(row, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(this, "Deactivate this user?", "Confirm", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            dao.UserDAO.deactivateUser(userId);

            JOptionPane.showMessageDialog(this, "User Deactivated");

            loadUsers("all");
        }

    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
  

    String keyword = searchField.getText().trim();

    try {
        ResultSet rs;

        // 🔥 KEY LOGIC
        if(keyword.isEmpty()) {
            rs = dao.UserDAO.getAllUsers();   // show all
        } else {
            rs = dao.UserDAO.searchUsers(keyword); // search
        }

        DefaultTableModel model =
            (DefaultTableModel) usersTable.getModel();

        model.setRowCount(0);

        while(rs.next()) {

            String status =
                rs.getString("approval_status") + " / " +
                (rs.getInt("is_active") == 1 ? "Active" : "Inactive");

            model.addRow(new Object[]{
                rs.getInt("user_id"),
                rs.getString("name"),
                rs.getString("email"),
                rs.getString("phone"),
                rs.getString("role"),
                status,
                rs.getTimestamp("created_at")
            });
        }

    } catch(Exception e) {
        e.printStackTrace();
    }

    }//GEN-LAST:event_searchBtnActionPerformed

