    public void loadStaff() {
    try {
        java.sql.ResultSet rs = dao.UserDAO.getAllStaff();

        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) staffTable.getModel();

        model.setRowCount(0);

        while(rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("staff_id"),
                rs.getString("name"),
                rs.getString("role"),
                rs.getString("phone"),
                rs.getString("shift"),
                rs.getDouble("salary"),
                rs.getDate("joining_date")
            });
        }

    } catch(Exception e) {
        e.printStackTrace();
    }
}

