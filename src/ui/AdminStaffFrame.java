    private void updateStaffBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateStaffBtnActionPerformed
        // TODO add your handling code here:

    int row = staffTable.getSelectedRow();

    if(row == -1) {
        JOptionPane.showMessageDialog(this, "Select staff first");
        return;
    }

    int staffId = Integer.parseInt(staffTable.getValueAt(row, 0).toString());

    String name = JOptionPane.showInputDialog(this, "Name", staffTable.getValueAt(row, 1));
    String role = JOptionPane.showInputDialog(this, "Role", staffTable.getValueAt(row, 2));
    String phone = JOptionPane.showInputDialog(this, "Phone", staffTable.getValueAt(row, 3));
    String shift = JOptionPane.showInputDialog(this, "Shift", staffTable.getValueAt(row, 4));
    String salaryStr = JOptionPane.showInputDialog(this, "Salary", staffTable.getValueAt(row, 5));

    if(name == null || role == null || phone == null || shift == null || salaryStr == null) return;

    double salary;

    try {
        salary = Double.parseDouble(salaryStr);
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Invalid salary");
        return;
    }

    dao.UserDAO.updateStaff(staffId, name, role, phone, shift, salary);

    JOptionPane.showMessageDialog(this, "Staff Updated");

    loadStaff();

    }//GEN-LAST:event_updateStaffBtnActionPerformed

    private void deleteStaffBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteStaffBtnActionPerformed
        // TODO add your handling code here:
       int row = staffTable.getSelectedRow();

    if(row == -1) {
        JOptionPane.showMessageDialog(this, "Select staff first");
        return;
    }

    int staffId = Integer.parseInt(staffTable.getValueAt(row, 0).toString());

    int confirm = JOptionPane.showConfirmDialog(this, "Delete this staff?", "Confirm", JOptionPane.YES_NO_OPTION);

    if(confirm == JOptionPane.YES_OPTION) {

        dao.UserDAO.deleteStaff(staffId);

        JOptionPane.showMessageDialog(this, "Staff Deleted");

        loadStaff();
    }

    }//GEN-LAST:event_deleteStaffBtnActionPerformed

