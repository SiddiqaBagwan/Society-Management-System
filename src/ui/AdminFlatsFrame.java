    private void updateFlatBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateFlatBtnActionPerformed
        // TODO add your handling code here:      
    int row = flatsTable.getSelectedRow();

    if(row == -1) {
        JOptionPane.showMessageDialog(this, "Select a flat first");
        return;
    }

    int flatId = Integer.parseInt(flatsTable.getValueAt(row, 0).toString());

    String currentBlock = flatsTable.getValueAt(row, 1).toString();
    String currentFlatNo = flatsTable.getValueAt(row, 2).toString();
    int currentFloor = Integer.parseInt(flatsTable.getValueAt(row, 3).toString());

    String newBlock = JOptionPane.showInputDialog(this, "Enter block", currentBlock);
    String newFlatNo = JOptionPane.showInputDialog(this, "Enter flat number", currentFlatNo);
    String floorStr = JOptionPane.showInputDialog(this, "Enter floor", currentFloor);

    if(newBlock == null || newFlatNo == null || floorStr == null) return;

    int newFloor;

    try {
        newFloor = Integer.parseInt(floorStr);
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Invalid floor");
        return;
    }

    boolean success = dao.UserDAO.updateFlat(flatId, newBlock, newFlatNo, newFloor);

if(success) {
    JOptionPane.showMessageDialog(this, "Flat Updated");
    loadFlats();
} else {
    JOptionPane.showMessageDialog(this, "Flat already exists in this block");
}

    loadFlats(); // refresh table

     
    }//GEN-LAST:event_updateFlatBtnActionPerformed

    private void deleteFlatBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteFlatBtnActionPerformed
        // TODO add your handling code here:

    int row = flatsTable.getSelectedRow();

    if(row == -1) {
        JOptionPane.showMessageDialog(this, "Select a flat first");
        return;
    }

    int flatId = Integer.parseInt(flatsTable.getValueAt(row, 0).toString());

    int confirm = JOptionPane.showConfirmDialog(this, "Delete this flat?", "Confirm", JOptionPane.YES_NO_OPTION);

    if(confirm == JOptionPane.YES_OPTION) {

        boolean success = dao.UserDAO.deleteFlat(flatId);

        if(success) {
            JOptionPane.showMessageDialog(this, "Flat Deleted");
            loadFlats();
        } else {
            JOptionPane.showMessageDialog(this, "Flat is currently assigned, cannot delete");
        }
    }

    }//GEN-LAST:event_deleteFlatBtnActionPerformed

