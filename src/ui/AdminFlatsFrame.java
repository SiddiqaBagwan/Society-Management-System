    public void loadFlats() {
    try {
        ResultSet rs = dao.UserDAO.getFlatDetails();

        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) flatsTable.getModel();

        model.setRowCount(0);

        while(rs.next()) {

            String user = rs.getString("name");

            model.addRow(new Object[]{
                rs.getInt("flat_id"),
                rs.getString("block"),
                rs.getString("flat_number"),
                rs.getInt("floor"),
                user == null ? "-" : user,
                rs.getString("type") == null ? "-" : rs.getString("type"),
                rs.getDate("start_date") == null ? "-" : rs.getDate("start_date")
            });
        }

    } catch(Exception e) {
        e.printStackTrace();
    }
}

