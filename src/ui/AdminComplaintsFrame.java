    private void resolveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resolveBtnActionPerformed
        // TODO add your handling code here:
        int row = complaintTable.getSelectedRow();

if(row == -1) {
    JOptionPane.showMessageDialog(this, "Select complaint first");
    return;
}

int complaintId = Integer.parseInt(
    complaintTable.getValueAt(row, 0).toString()
);

dao.UserDAO.resolveComplaint(complaintId);

JOptionPane.showMessageDialog(this, "Complaint Resolved");

loadComplaints(); // refresh
    }//GEN-LAST:event_resolveBtnActionPerformed

