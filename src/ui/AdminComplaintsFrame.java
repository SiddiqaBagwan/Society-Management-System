    public void loadComplaints() {
        try {
            ResultSet rs = dao.UserDAO.getAllComplaints();

            javax.swing.table.DefaultTableModel model
                    = (javax.swing.table.DefaultTableModel) complaintTable.getModel();

            model.setRowCount(0);

            while (rs.next()) {

                Object resolvedDate = rs.getDate("resolved_date");

                model.addRow(new Object[]{
                    rs.getInt("complaint_id"),
                    rs.getString("name"),
                    rs.getString("block") + "-" + rs.getString("flat_number"),
                    rs.getString("description"),
                    rs.getString("priority"),
                    rs.getString("status"),
                    rs.getDate("date_created"),
                    resolvedDate == null ? "-" : resolvedDate
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

