    public void loadUsers() {
        try {
            java.sql.ResultSet rs = dao.UserDAO.getAllUsers();

            userBox.removeAllItems(); // remove "Item 1"

            while (rs.next()) {
                userBox.addItem(rs.getInt("user_id") + " - " + rs.getString("name"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadFlats() {
        try {
            java.sql.ResultSet rs = dao.UserDAO.getAllFlats();

            flatBox.removeAllItems();

            while (rs.next()) {
                flatBox.addItem(rs.getInt("flat_id") + " - "
                        + rs.getString("block") + "-" + rs.getString("flat_number"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

