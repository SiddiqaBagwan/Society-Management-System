    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        // TODO add your handling code here:
        String user = userBox.getSelectedItem().toString();
        String flat = flatBox.getSelectedItem().toString();
        String type = typeBox.getSelectedItem().toString();

        int userId = Integer.parseInt(user.split(" - ")[0]);
        int flatId = Integer.parseInt(flat.split(" - ")[0]);

// 🔥 optional info
        if (dao.UserDAO.isFlatAssigned(flatId)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Flat already assigned. Reassigning...");
        }

        dao.UserDAO.assignFlat(userId, flatId, type);

        javax.swing.JOptionPane.showMessageDialog(this, "Flat Assigned");

// go back
        new AdminFlatsFrame(currentEmail).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_assignBtnActionPerformed

